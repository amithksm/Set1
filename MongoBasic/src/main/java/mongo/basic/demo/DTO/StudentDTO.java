package mongo.basic.demo.DTO;

public class StudentDTO {
	
	String rollNum;
	String fName;
	String lName;
	String address1;
	String address2;
	int age;
	String gender;
	
	public String getRollNum() {
		return rollNum;
	}
	public String getfName() {
		return fName;
	}
	public String getlName() {
		return lName;
	}
	public String getAddress1() {
		return address1;
	}
	public String getAddress2() {
		return address2;
	}
	public int getAge() {
		return age;
	}
	public String getGender() {
		return gender;
	}
	
}
